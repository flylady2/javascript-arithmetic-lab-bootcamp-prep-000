function add(x,y) {
  return x + y
}
function subtract(a,b) {
  return a - b
}
function multiply (x,y) {
  return x * y
}
function divide (c, d) {
  return c/d
}
function inc(n) {
  return n +=1
}
function dec(n) {
  return n -=1
}
function makeInt(n) {
  parseInt('n', 10);
  return n
}
function preserveDecimal(n) {
  parseFloat('n');
  return n
}
